import { a, defineData, type ClientSchema } from '@aws-amplify/backend';

const schema = a.schema({
  // ==================== User Management ====================
  Department: a.model({
    id: a.id().required(),
    name: a.string().required(),
    description: a.string(),
    status: a.enum(['active', 'inactive']),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
    roles: a.hasMany('Role', 'departmentId'),
    users: a.hasMany('User', 'departmentId'),
  }).authorization((allow) => [allow.authenticated()]),

  Role: a.model({
    id: a.id().required(),
    name: a.string().required(),
    description: a.string(),
    departmentId: a.id(),
    department: a.belongsTo('Department', 'departmentId'),
    status: a.enum(['active', 'inactive']),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
    users: a.hasMany('User', 'roleId'),
    permissions: a.hasMany('Permission', 'roleId'),
  }).authorization((allow) => [allow.authenticated()]),

  User: a.model({
    id: a.id().required(),
    firstName: a.string().required(),
    lastName: a.string().required(),
    email: a.email().required(),
    employeeId: a.string(),
    departmentId: a.id(),
    roleId: a.id(),
    department: a.belongsTo('Department', 'departmentId'),
    role: a.belongsTo('Role', 'roleId'),
    lineManager: a.string(),
    phone: a.string(),
    status: a.enum(['active', 'inactive', 'on_leave']),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  Permission: a.model({
    id: a.id().required(),
    roleId: a.id().required(),
    role: a.belongsTo('Role', 'roleId'),
    moduleName: a.string().required(),
    canRead: a.boolean().required(),
    canCreate: a.boolean().required(),
    canUpdate: a.boolean().required(),
    canDelete: a.boolean().required(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Customer Management ====================
  Customer: a.model({
    id: a.id().required(),
    name: a.string().required(),
    email: a.email(),
    mobile: a.string(),
    address: a.string(),
    status: a.enum(['active', 'inactive', 'suspended']),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
    vehicles: a.hasMany('Vehicle', 'customerId'),
    jobOrders: a.hasMany('JobOrder', 'customerId'),
    contacts: a.hasMany('Contact', 'customerId'),
  }).authorization((allow) => [allow.authenticated()]),

  Contact: a.model({
    id: a.id().required(),
    firstName: a.string().required(),
    lastName: a.string().required(),
    email: a.email().required(),
    phone: a.string(),
    customerId: a.id().required(),
    customer: a.belongsTo('Customer', 'customerId'),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  Vehicle: a.model({
    id: a.id().required(),
    make: a.string().required(),
    model: a.string().required(),
    year: a.string().required(),
    color: a.string(),
    plateNumber: a.string(),
    vin: a.string(),
    vehicleType: a.string(),
    customerId: a.id().required(),
    customer: a.belongsTo('Customer', 'customerId'),
    status: a.enum(['active', 'inactive', 'retired']),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
    jobOrders: a.hasMany('JobOrder', 'vehicleId'),
    inspections: a.hasMany('Inspection', 'vehicleId'),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Job Order Management ====================
  JobOrder: a.model({
    id: a.id().required(),
    orderNumber: a.string().required(),
    customerId: a.id().required(),
    vehicleId: a.id().required(),
    customer: a.belongsTo('Customer', 'customerId'),
    vehicle: a.belongsTo('Vehicle', 'vehicleId'),
    workStatus: a.enum(['New Request', 'Inspection', 'Inprogress', 'Quality Check', 'Ready', 'Completed', 'Cancelled']),
    paymentStatus: a.enum(['Fully Paid', 'Partially Paid', 'Unpaid', 'Cancelled']),
    totalAmount: a.float(),
    paidAmount: a.float(),
    createdDate: a.datetime(),
    completionDate: a.datetime(),
    createdByUserId: a.id(),
    assignedToUserId: a.id(),
    serviceRequests: a.hasMany('ServiceRequest', 'jobOrderId'),
    payments: a.hasMany('Payment', 'jobOrderId'),
    exitPermit: a.hasOne('ExitPermit', 'jobOrderId'),
    qualityChecks: a.hasMany('QualityCheck', 'jobOrderId'),
    inspections: a.hasMany('Inspection', 'jobOrderId'),
    additionalServiceRequests: a.hasMany('AdditionalServiceRequest', 'jobOrderId'),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  ServiceRequest: a.model({
    id: a.id().required(),
    jobOrderId: a.id().required(),
    jobOrder: a.belongsTo('JobOrder', 'jobOrderId'),
    serviceName: a.string().required(),
    status: a.enum(['Pending', 'Inprogress', 'Completed', 'Cancelled']),
    startTime: a.datetime(),
    endTime: a.datetime(),
    duration: a.string(),
    assignedTechnician: a.string(),
    notes: a.string(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  AdditionalServiceRequest: a.model({
    id: a.id().required(),
    jobOrderId: a.id().required(),
    jobOrder: a.belongsTo('JobOrder', 'jobOrderId'),
    serviceName: a.string().required(),
    description: a.string(),
    estimatedCost: a.float(),
    status: a.enum(['Requested', 'Approved', 'Rejected', 'Completed']),
    requestedBy: a.string(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  Payment: a.model({
    id: a.id().required(),
    jobOrderId: a.id().required(),
    jobOrder: a.belongsTo('JobOrder', 'jobOrderId'),
    amount: a.float().required(),
    paymentMethod: a.enum(['Cash', 'Card', 'Check', 'Bank Transfer', 'Online']),
    paymentStatus: a.enum(['Pending', 'Completed', 'Failed', 'Refunded']),
    paymentDate: a.datetime(),
    referenceNumber: a.string(),
    notes: a.string(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  ExitPermit: a.model({
    id: a.id().required(),
    jobOrderId: a.id().required(),
    jobOrder: a.belongsTo('JobOrder', 'jobOrderId'),
    permitNumber: a.string(),
    status: a.enum(['Not Created', 'Created', 'Collected']),
    createdDate: a.datetime(),
    collectedDate: a.datetime(),
    nextServiceDate: a.datetime(),
    createdByUserId: a.id(),
    collectedByUserId: a.id(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Inspection & Quality ====================
  Inspection: a.model({
    id: a.id().required(),
    jobOrderId: a.id(),
    vehicleId: a.id(),
    jobOrder: a.belongsTo('JobOrder', 'jobOrderId'),
    vehicle: a.belongsTo('Vehicle', 'vehicleId'),
    inspectionType: a.string().required(),
    status: a.enum(['Pending', 'In Progress', 'Completed', 'Failed']),
    inspectorId: a.id(),
    findings: a.string(),
    checklist: a.json(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  QualityCheck: a.model({
    id: a.id().required(),
    jobOrderId: a.id().required(),
    jobOrder: a.belongsTo('JobOrder', 'jobOrderId'),
    status: a.enum(['Pending', 'In Progress', 'Passed', 'Failed']),
    inspectorId: a.id(),
    checklist: a.json(),
    issues: a.string(),
    approvalStatus: a.enum(['Not Required', 'Pending', 'Approved', 'Rejected']),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Sales Management ====================
  SalesLead: a.model({
    id: a.id().required(),
    dealName: a.string().required(),
    amount: a.float(),
    stage: a.enum(['Lead', 'Prospect', 'Proposal', 'Negotiation', 'Won', 'Lost']),
    customerId: a.id(),
    customer: a.belongsTo('Customer', 'customerId'),
    ownerId: a.id(),
    probability: a.float(),
    expectedCloseDate: a.datetime(),
    notes: a.string(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
    approvals: a.hasMany('SalesApproval', 'leadId'),
  }).authorization((allow) => [allow.authenticated()]),

  SalesApproval: a.model({
    id: a.id().required(),
    leadId: a.id().required(),
    lead: a.belongsTo('SalesLead', 'leadId'),
    approverUserId: a.id().required(),
    status: a.enum(['Pending', 'Approved', 'Rejected']),
    comments: a.string(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Product Catalog ====================
  Product: a.model({
    id: a.id().required(),
    name: a.string().required(),
    description: a.string(),
    suvPrice: a.float(),
    sedanPrice: a.float(),
    otherPrice: a.float(),
    category: a.string(),
    status: a.enum(['active', 'inactive']),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Purchase Management ====================
  PurchaseOrder: a.model({
    id: a.id().required(),
    orderNumber: a.string().required(),
    supplierId: a.id(),
    supplier: a.belongsTo('Supplier', 'supplierId'),
    supplierName: a.string().required(),
    status: a.enum(['Draft', 'Submitted', 'Approved', 'Received', 'Cancelled']),
    totalAmount: a.float(),
    createdDate: a.datetime(),
    deliveryDate: a.datetime(),
    items: a.json(),
    notes: a.string(),
    createdByUserId: a.id(),
    approverUserId: a.id(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Service Execution ====================
  ServiceExecution: a.model({
    id: a.id().required(),
    jobOrderId: a.id(),
    serviceType: a.string().required(),
    status: a.enum(['Scheduled', 'In Progress', 'Completed', 'On Hold', 'Cancelled']),
    executorId: a.id(),
    startTime: a.datetime(),
    endTime: a.datetime(),
    notes: a.string(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Company/CRM ====================
  Company: a.model({
    id: a.id().required(),
    name: a.string().required(),
    industry: a.string(),
    email: a.email(),
    phone: a.string(),
    address: a.string(),
    status: a.enum(['active', 'inactive']),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Invoice Management ====================
  Invoice: a.model({
    id: a.id().required(),
    invoiceNumber: a.string().required(),
    jobOrderId: a.id().required(),
    jobOrder: a.belongsTo('JobOrder', 'jobOrderId'),
    customerId: a.id().required(),
    customer: a.belongsTo('Customer', 'customerId'),
    invoiceDate: a.datetime().required(),
    dueDate: a.datetime(),
    subtotal: a.float().required(),
    taxAmount: a.float(),
    totalAmount: a.float().required(),
    paidAmount: a.float(),
    status: a.enum(['Draft', 'Sent', 'Viewed', 'Partial', 'Paid', 'Overdue', 'Cancelled']),
    notes: a.string(),
    createdByUserId: a.id(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Supplier Management ====================
  Supplier: a.model({
    id: a.id().required(),
    name: a.string().required(),
    email: a.email(),
    phone: a.string(),
    address: a.string(),
    city: a.string(),
    country: a.string(),
    zipCode: a.string(),
    contactPersonName: a.string(),
    paymentTerms: a.string(),
    taxId: a.string(),
    status: a.enum(['active', 'inactive', 'suspended']),
    rating: a.float(),
    notes: a.string(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
    purchaseOrders: a.hasMany('PurchaseOrder', 'supplierId'),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Approval Workflow ====================
  ApprovalRequest: a.model({
    id: a.id().required(),
    title: a.string().required(),
    type: a.enum(['ServiceApproval', 'PurchaseApproval', 'PaymentApproval', 'QualityApproval', 'Other']),
    entityId: a.id().required(),
    entityType: a.string().required(),
    requestedByUserId: a.id().required(),
    approverUserId: a.id().required(),
    status: a.enum(['Pending', 'Approved', 'Rejected', 'Escalated']),
    priority: a.enum(['Low', 'Medium', 'High', 'Critical']),
    description: a.string(),
    approvalNotes: a.string(),
    requestDate: a.datetime().required(),
    responseDate: a.datetime(),
    dueDate: a.datetime(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Service Approval ====================
  ServiceApprovalRequest: a.model({
    id: a.id().required(),
    jobOrderId: a.id().required(),
    jobOrder: a.belongsTo('JobOrder', 'jobOrderId'),
    serviceRequestId: a.id(),
    description: a.string().required(),
    estimatedCost: a.float(),
    status: a.enum(['Pending', 'Approved', 'Rejected', 'Under Review']),
    requestedByUserId: a.id().required(),
    approverUserId: a.id().required(),
    rejectionReason: a.string(),
    requestDate: a.datetime().required(),
    approvalDate: a.datetime(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Quotation & Estimates ====================
  Quotation: a.model({
    id: a.id().required(),
    quotationNumber: a.string().required(),
    jobOrderId: a.id(),
    customerId: a.id().required(),
    customer: a.belongsTo('Customer', 'customerId'),
    description: a.string(),
    items: a.json(),
    subtotal: a.float().required(),
    taxAmount: a.float(),
    totalAmount: a.float().required(),
    validUntil: a.datetime(),
    status: a.enum(['Draft', 'Sent', 'Accepted', 'Rejected', 'Expired']),
    createdByUserId: a.id(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Activity & Audit Log ====================
  ActivityLog: a.model({
    id: a.id().required(),
    userId: a.id().required(),
    user: a.belongsTo('User', 'userId'),
    action: a.string().required(),
    entityType: a.string().required(),
    entityId: a.id().required(),
    changes: a.json(),
    ipAddress: a.string(),
    userAgent: a.string(),
    status: a.enum(['Success', 'Failed', 'Pending']),
    notes: a.string(),
    createdAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Work Log & Time Tracking ====================
  WorkLog: a.model({
    id: a.id().required(),
    jobOrderId: a.id().required(),
    jobOrder: a.belongsTo('JobOrder', 'jobOrderId'),
    technicianId: a.id().required(),
    serviceType: a.string().required(),
    startTime: a.datetime().required(),
    endTime: a.datetime(),
    duration: a.float(),
    description: a.string(),
    status: a.enum(['Started', 'In Progress', 'Paused', 'Completed', 'Cancelled']),
    notes: a.string(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),

  // ==================== Notifications ====================
  Notification: a.model({
    id: a.id().required(),
    userId: a.id().required(),
    user: a.belongsTo('User', 'userId'),
    type: a.enum(['Info', 'Warning', 'Error', 'Success', 'Approval', 'Task']),
    title: a.string().required(),
    message: a.string().required(),
    entityType: a.string(),
    entityId: a.id(),
    isRead: a.boolean(),
    readAt: a.datetime(),
    actionUrl: a.string(),
    priority: a.enum(['Low', 'Medium', 'High']),
    expiresAt: a.datetime(),
    createdAt: a.datetime(),
    updatedAt: a.datetime(),
  }).authorization((allow) => [allow.authenticated()]),
});

export type Schema = ClientSchema<typeof schema>;

export const data = defineData({
  schema,
  authorizationModes: {
    defaultAuthorizationMode: 'userPool',
  },
});
